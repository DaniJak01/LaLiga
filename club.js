document.addEventListener("DOMContentLoaded", function() {
    const urlParams = new URLSearchParams(window.location.search);
    const teamName = urlParams.get('team');

    const team = teamsData[teamName];

    if (team) {
        document.getElementById('club-logo').src = team.logo;
        document.getElementById('club-name').textContent = team.name;
        document.getElementById('stadium').textContent = team.stadium;
        document.getElementById('stadiumImg').src = team.stadiumImg;
        document.getElementById('titles').textContent = team.titles;
        document.getElementById('foundation').textContent = team.foundationYear;
        document.getElementById('shirtImg').src = team.shirtImg;

        const logoContainer = document.querySelector('.logo-container');
        if (logoContainer) {
            logoContainer.style.backgroundImage = `url(${team.background})`;
            logoContainer.style.backgroundSize = 'cover';
            logoContainer.style.backgroundPosition = 'center';
            logoContainer.style.backgroundRepeat = 'no-repeat';
        }
    } else {
        document.getElementById('club-info').innerHTML = "<p>Equipo no encontrado.</p>";
    }
});

const shirtBtn = document.getElementById('shirt');
console.log(shirtBtn);

shirtBtn.addEventListener("click", () => {
    const urlParams = new URLSearchParams(window.location.search);
    const teamName = urlParams.get('team');

    const team = teamsData[teamName];

    if (team) {
        window.location.href = `product.html?productId=${teamName}-shirt-1`;
        
    } else {
        document.getElementById('shirt-info').innerHTML = "<p>Camiseta no encontrada.</p>";
    }
});
